#include <stdio.h>
#include <math.h>

int main() {
    double A, B, C, r, S;

    // Введення кутів трикутника (в градусах) і радіуса вписаного кола
    printf("Введіть кут A (в градусах): ");
    scanf("%lf", &A);
    
    printf("Введіть кут B (в градусах): ");
    scanf("%lf", &B);
    
    printf("Введіть кут C (в градусах): ");
    scanf("%lf", &C);
    
    printf("Введіть радіус вписаного кола (r): ");
    scanf("%lf", &r);
    
    // Переведення кутів з градусів у радіани для використання в тригонометричних функціях
    A = A * M_PI / 180;
    B = B * M_PI / 180;
    C = C * M_PI / 180;
    
    // Периметр трикутника пропорційний сумі синусів кутів
    double sum_of_sines = sin(A) + sin(B) + sin(C);
    
    // Площа трикутника
    S = r * sum_of_sines;
    
    // Виведення результату
    printf("Площа трикутника: %.2f\n", S);
    
    return 0;
}
