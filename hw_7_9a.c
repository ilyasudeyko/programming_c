#include <stdio.h>

int main() {
    int n;
    double max, current;

    // Введення кількості чисел
    printf("Введіть кількість чисел (n): ");
    scanf("%d", &n);

    if (n <= 0) {
        printf("Кількість чисел повинна бути більшою за 0.\n");
        return 1;
    }

    // Введення першого числа
    printf("Введіть число 1: ");
    scanf("%lf", &max); // Припускаємо, що перше число є максимальним

    // Введення наступних чисел і пошук максимуму
    for (int i = 2; i <= n; i++) {
        printf("Введіть число %d: ", i);
        scanf("%lf", &current);

        if (current > max) {
            max = current; // Оновлення максимуму
        }
    }

    // Виведення результату
    printf("Максимальне число: %.10f\n", max);

    return 0;
}
